        private void Delete (int Id)
	{
		try
		{
			//prepare statement
			String qry = "Delete from MediInfo where MedicineID = ?";
			PreparedStatement ps = con.prepareStatement(qry);
			ps.setInt(1, Id);

			ps.executeUpdate();
		}
		catch(Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
	}
    
    
    
    	private void Update (int Id, String N, int Q, float P, int D,String M)
	{
		try
		{
			//prepare statement
			String qry = "Update MediInfo set MedicineID = ?, MediTitle = ?, Qty = ?, Price = ?, DosAge = ?, Make = ? where MedicineID = ?";
			PreparedStatement ps = con.prepareStatement(qry);

			ps.setInt(1, Id);
			ps.setString(2, N);
			ps.setInt(3, Q);
			ps.setFloat(4, P);
                        ps.setInt(5, D);
			ps.setString(6, M);
                        
                        ps.setInt(7, Id);
			ps.executeUpdate();
		}
		catch(Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
	}
    

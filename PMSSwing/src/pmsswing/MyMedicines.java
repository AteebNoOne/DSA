    private void data_comboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_data_comboActionPerformed
        // TODO add your handling code here:
        String MedN,MedM;
        int MedQ,MedD;
        float MedP;
        String select = data_combo.getSelectedItem().toString();
        try{        
            con = DriverManager.getConnection("jdbc:ucanaccess://D:\\DSA Theory Summers\\Project\\ODBC connectivity issue resolved\\MediDB.accdb");
            //create statement
            st = con.createStatement();  
            ResultSet myRes = st.executeQuery("Select * from MediInfo where MedicineId="+select);
        
        while (myRes.next()){
            MedN = myRes.getString("MediTitle"); 
            MedQ = myRes.getInt("Qty");
            MedP = myRes.getFloat("Price");
            MedD = myRes.getInt("DosAge");
            MedM = myRes.getString("Make");
            data_name.setText(MedN);
            data_quan.setText(String.valueOf(MedQ));
            data_price.setText(String.valueOf(MedP));
            data_dosage.setText(String.valueOf(MedD));
            data_make.setText(MedM);
        }
        con.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_data_comboActionPerformed


    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // adding to database
        String N,M;
        int Id,Q,D;
        float P;
        Id=Integer.parseInt(addMed_id.getText());
        N=addMed_name.getText();
        Q=Integer.parseInt(addMed_quan.getText());
        P=Float.parseFloat(addMed_price.getText());
        D=Integer.parseInt(addMed_dosage.getText());
        M=addMed_make.getText();
        //Check if any feild is empty?
        if(Id == 0 || N.isEmpty() || Q == 0 || P == 0 || D == 0 || M.isEmpty() ){
            JOptionPane.showMessageDialog(null, "All Fields must be filled");
            return;
        }
            if(JOptionPane.showConfirmDialog(null,"Are you sure, you want to add "+addMed_name.getText()+"?","Pharmacy Management System",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_NO_OPTION){
            ConnectToDatabase();
        Add(Id,N,Q,P,D,M);
        Display(table);
        addMed_id.setText("");
        addMed_price.setText("");
        addMed_name.setText("");
        addMed_quan.setText("");
        addMed_dosage.setText("");
        addMed_make.setText("");
        }
    }//GEN-LAST:event_jButton8ActionPerformed

